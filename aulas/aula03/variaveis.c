#include<stdio.h>

int main() {
  char tecla = 'a';
  printf("O valor de tecla = %c\n", tecla);
  tecla ='F';
  printf("O valor de tecla = %c\n", tecla);

  int idade = 18;
  printf("O valor de idade = %i\n", idade);

  float media_final= 1.7f;
  printf("O valor de media = %f\n", media_final);

  double pi = 3.14159265358979323846f;
  printf("O valor de pi = %f\n", pi);
  
  return 0;
}